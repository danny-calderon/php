function showMap($map) {
    foreach ($map as $row) {
        echo implode(" ", $row) . "\n";
    }
}

function markImpacts(&$map, $impacts) {
    foreach ($impacts as $impact) {
        [$x, $y] = $impact;
        if ($map[$x][$y] === 'A') {
            $map[$x][$y] = 'C';
        } elseif ($map[$x][$y] === '0') {
            $map[$x][$y] = 'X';
        } elseif ($map[$x][$y] === '~') {
            $map[$x][$y] = 'S';
        }
    }
}

function calculateAffectedPeople($map) {
    $affected = 0;
    foreach ($map as $row) {
        foreach ($row as $cell) {
            if ($cell === 'C') {
                $affected += 5000;
            }
        }
    }
    return $affected;
}

function calculateDamages($map) {
    $urbanDamage = 0;
    $nonUrbanDamage = 0;

    foreach ($map as $row) {
        foreach ($row as $cell) {
            if ($cell === 'C') {
                $urbanDamage += 200000;
            } elseif ($cell === 'X') {
                $nonUrbanDamage += 50000;
            }
        }
    }

    return [$urbanDamage, $nonUrbanDamage, $urbanDamage + $nonUrbanDamage];
}

function calculateSeaArea($map) {
    $seaArea = 0;
    foreach ($map as $row) {
        foreach ($row as $cell) {
            if ($cell === '~') {
                $seaArea++;
            }
        }
    }
    return $seaArea;
}

function calculateImpactedSea($map) {
    $impactedSea = 0;
    foreach ($map as $row) {
        foreach ($row as $cell) {
            if ($cell === 'S') {
                $impactedSea++;
            }
        }
    }
    return $impactedSea;
}

function calculateONGRevenue($impactedSeaArea) {
    $totalFish = ($impactedSeaArea / calculateSeaArea($GLOBALS['pomodoroHaters'])) * 2000;
    return $totalFish * 5 * 1000; // En euros
}

